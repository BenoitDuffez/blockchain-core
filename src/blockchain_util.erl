%% == Blockchain Utility Functions ==
%% @end
%%%-------------------------------------------------------------------
-module(blockchain_util).

-export([
    hexdump/1
]).

%%--------------------------------------------------------------------
%% @doc
%% @end
%%--------------------------------------------------------------------
-spec hexdump(binary()) -> string().
hexdump(Bin) ->
    lists:flatten([[io_lib:format("~2.16.0b",[X]) || <<X:8>> <= Bin]]).
